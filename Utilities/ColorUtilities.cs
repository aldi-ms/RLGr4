namespace RLG.Utilities
{
    using Microsoft.Xna.Framework;
    using System;

    public static class ColorUtilities
    {
        /// <summary>
        /// Convert XNA Color object to UInt number.
        /// </summary>
        /// <param name="color">Color we want to parse to UInt.</param>
        /// <returns>The UInt representing the Color.</returns>
        public static uint ToUInt(this Color color)
        {
            return (uint)((color.A << 24) | (color.R << 16) |
                          (color.G << 8) | (color.B << 0));
        }

        /// <summary>
        /// Convert UInt number to XNA Color object.
        /// </summary>
        /// <param name="color">UInt we want to parse to Color.</param>
        /// <returns>XNA Color object.</returns>
        public static Color ToColor(this uint color)
        {
            byte a = (byte)(color >> 24);
            byte r = (byte)(color >> 16);
            byte g = (byte)(color >> 8);
            byte b = (byte)(color >> 0);

            return new Color(r, g, b, a);
        }

        // Given H,S,L in range of 0-1
        // Returns a Color (RGB struct) in range of 0-255
        public static Color HSL2RGB(double h, double sl, double l)
        {
            double v;
            double r, g, b;

            r = l;   // default to gray
            g = l;
            b = l;
            v = (l <= 0.5) ? (l * (1.0 + sl)) : (l + sl - l * sl);

            if (v > 0)
            {
                double m;
                double sv;
                int sextant;
                double fract, vsf, mid1, mid2;

                m = l + l - v;
                sv = (v - m) / v;
                h *= 6.0;
                sextant = (int)h;
                fract = h - sextant;
                vsf = v * sv * fract;
                mid1 = m + vsf;
                mid2 = v - vsf;

                switch (sextant)
                {
                    case 0:
                        r = v;
                        g = mid1;
                        b = m;
                        break;

                    case 1:
                        r = mid2;
                        g = v;
                        b = m;
                        break;

                    case 2:
                        r = m;
                        g = v;
                        b = mid1;
                        break;

                    case 3:
                        r = m;
                        g = mid2;
                        b = v;
                        break;

                    case 4:
                        r = mid1;
                        g = m;
                        b = v;
                        break;

                    case 5:
                        r = v;
                        g = m;
                        b = mid2;
                        break;

                }
            }

            byte red = Convert.ToByte(r * 255.0f);
            byte green = Convert.ToByte(g * 255.0f);
            byte blue = Convert.ToByte(b * 255.0f);

            return new Color(red, green, blue);

        }
    }
}
